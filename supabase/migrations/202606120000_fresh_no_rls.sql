      select 1
      from public.matakuliah course
      where course.kelas_id = target_class_id
        and course.lecturer_id = auth.uid()
    );
$$;

create or replace function public.join_class(access_code text)
returns bigint
language plpgsql
security definer
set search_path = public
as $$
declare
  target_class public.kelas%rowtype;
  assigned_role text := 'student';
  current_email text := coalesce(auth.jwt() ->> 'email', '');
  current_profile public.profiles%rowtype;
begin
  if auth.uid() is null or trim(current_email) = '' then
    raise exception 'Authentication required';
  end if;

  select *
  into target_class
  from public.kelas classroom
  where upper(classroom.kode_kelas) = upper(trim(access_code))
     or upper(classroom.kode_wakil) = upper(trim(access_code))
  limit 1;

  if not found then
    raise exception 'Kode kelas tidak valid';
  end if;
  if not target_class.is_open then
    raise exception 'Kelas sedang ditutup';
  end if;
  if upper(target_class.kode_wakil) = upper(trim(access_code)) then
    assigned_role := 'class_leader';
  end if;

  select *
  into current_profile
  from public.profiles
  where id = auth.uid();

  insert into public.anggota_kelas (
    kelas_id,
    user_email,
    role,
    user_name,
    nim,
    avatar_url
  )
  values (
    target_class.id,
    current_email,
    assigned_role,
    coalesce(current_profile.display_name, 'Pengguna'),
    coalesce(current_profile.student_number, '-'),
    current_profile.avatar_url
  )
  on conflict do nothing;

  return target_class.id;
end;
$$;

