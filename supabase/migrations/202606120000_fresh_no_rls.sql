create or replace function public.current_user_role()
returns text
language sql
stable
security definer
set search_path = public
as $$
  select case
    when coalesce(
      (select role from public.profiles where id = auth.uid()),
      'student'
    ) = 'student'
    and exists (
      select 1
      from public.anggota_kelas member
      where lower(member.user_email) =
        lower(coalesce(auth.jwt() ->> 'email', ''))
        and lower(member.role) in (
          'admin',
          'ketua kelas',
          'ketua',
          'class_leader'
        )
    )
      then 'class_leader'
    else coalesce(
      (select role from public.profiles where id = auth.uid()),
      'student'
    )
  end;
$$;

