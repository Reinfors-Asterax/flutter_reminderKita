create table public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  email text not null unique,
  display_name text not null default 'Pengguna',
  student_number text,
  role text not null default 'student'
    check (role in ('admin', 'lecturer', 'class_leader', 'student')),
  requested_role text,
  approval_status text not null default 'active'
    check (approval_status in ('active', 'pending', 'rejected')),
  avatar_url text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table public.kelas (
  id bigint generated by default as identity primary key,
  nama_kelas text not null
    check (char_length(trim(nama_kelas)) between 1 and 200),
  kode_kelas text not null unique,
  kode_wakil text unique,
  created_by uuid not null
    references public.profiles(id) on delete cascade,
  is_open boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

